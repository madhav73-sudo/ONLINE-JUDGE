#include <iostream> // This line includes the iostream library, which provides input/output functionalities.

int main() { // This is the main function where the program execution begins.
    std::cout << "Hello World!" << std::endl; // This line prints "Hello World!" to the console.
                                             // std::cout is the standard output stream.
                                             // << is the insertion operator.
                                             // std::endl inserts a newline character and flushes the output buffer.
    return 0; // This line indicates that the program executed successfully.
}